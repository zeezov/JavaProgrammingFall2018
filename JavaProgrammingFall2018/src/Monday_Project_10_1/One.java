package Monday_Project_10_1;

public class One {
	public static void main (String[] args) {
		
		/*
		 * 1.Write for and while loops so that they print numbers between 1 and 20
		 */
		
		//while loop
		int num = 0;
		
		while (num < 20) {
			num++;
			System.out.print(num + " ");
		}
		System.out.println(" ");
		System.out.println("=========================");
		
		//for loop
		for (int i = 1; i <= 20; i++) {
			System.out.print(i + " ");
		}
		
		
		
	}


}
