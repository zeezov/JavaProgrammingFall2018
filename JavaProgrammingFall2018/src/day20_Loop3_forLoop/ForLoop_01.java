package day20_Loop3_forLoop;

public class ForLoop_01 {
	public static void main(String[] args) {
		
		for(int i = 1; i <= 5; i++) {
			
			System.out.println(i);
			System.out.println("Happy Birth Day!");
		}
		
		for (int j = 5; j <= 100; j += 5) { //Every 5 times
			System.out.println("");
			System.out.println("Congratulation " + j);
		}
		
		
	}

}
